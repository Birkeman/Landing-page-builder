    $(document).ready(function(){
        $('.js-project-pricing-number').on('change', function(){
            if($(this).val()==1){
                $('.js-project-pricing').addClass('d-none')
                $('.js-project-pricing-1').removeClass('d-none')
            }
            if($(this).val()==2){
                $('.js-project-pricing').addClass('d-none')
                $('.js-project-pricing-1, .js-project-pricing-2').removeClass('d-none')
            }
            if($(this).val()==3){
                $('.js-project-pricing').addClass('d-none')
                $('.js-project-pricing-1, .js-project-pricing-2, .js-project-pricing-3').removeClass('d-none')
            }
            if($(this).val()==0){
                $('.js-project-pricing').addClass('d-none')
            }
        })
        $('.js-project-benefits-1-number').on('change', function(){
            benefits = $(this).val();
            $('.js-project-plan-benefit-1').addClass('d-none');
            for (i = 0; i < 7; i++) {
                if(i==benefits){
                    console.log(benefits)
                    for (y = 0; y < i+1; y++){
                        $('.project-benefit-1-'+ y ).removeClass('d-none');
                    }
                }
            }
            if($(this).val()==0){
                $('.js-project-plan-benefit-1').addClass('d-none')
            }
        })
        $('.js-project-benefits-2-number').on('change', function(){
